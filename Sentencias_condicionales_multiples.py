print("Conversor de numeros a letras")
print("==============================")

num_uno = int(input("Selecciona el numero a convertir: "))

if num_uno == 1:
    print("UNO")
elif num_uno == 2:
    print("DOS")
elif num_uno == 3:
    print("TRES")
elif num_uno == 4:
    print("CUATRO")
elif num_uno == 5:
    print("CINCO")
else:
    print("Solo puedes convertir numeros del uno al cinco. ")
print("Fin.")    
    
